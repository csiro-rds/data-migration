CREATE OR REPLACE VIEW CMS_COLLECTION_ITEM_W_LABEL_V AS
	SELECT 
		CI.ACCESSIONDATE,
		CI.ACCESSIONNO,
		CI.ACCESSREASON,
		CI.ACCESSSTATUS,
		CI.BASISOFRECORD,
		CI.CONDITION,
		CI.COUNT,
		CI.COUNTQUALIFIER,
		CI.CREATEDATE,
		CI.CREATEDBY,
		CI.CURRENTSTATUS,
		CI.CURRENTSTATUSQUALIFIER,
		CI.DCTERMSTYPE,
		CI.DEACCESSIONDATE,
		CI.INTERNALCATALOGNUMBER,
		CI.INTERNALNOTE,
		CI.ITEMREMARKS,
		CI.ITEMTYPE,
		CI.LABELCOLLECTIONCODE,
		CI.LEGACYID_CI,
		CI.LEGACYID_CU,
		CI.LIFEFORM,
		CI.LIFESTAGE,
		CI.ORIGINOFRECORD,
		CI.RECORDSTATUS,
		CI.SEX,
		CI.SUFFIX,
		CI.UPDATEDATE,
		CI.UPDATEDBY,
		CONCAT(CI.LABELCOLLECTIONCODE, '-', CI.ACCESSIONNO, '.', CI.SUFFIX) AS CA_IDNO,
		CU.EXCHANGEUNITID
	FROM CMS_COLLECTION_ITEM_V CI LEFT JOIN CMS_COLLECTION_UNIT_V CU USING (LEGACYID_CU);
