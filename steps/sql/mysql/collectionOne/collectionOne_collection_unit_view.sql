CREATE OR REPLACE VIEW CMS_COLLECTION_UNIT_W_COLLECTORS_V AS
SELECT u.LEGACYID_CU,
	u.EXCHANGEUNITID,
	u.LEGACYID_CE,
	u.RECORDNUMBER,
	u.ESTABLISHMENTMEANS,
	u.LIFESTAGE,
	u.HABIT,
	u.ORGANISMQUANTITY,
	u.ORGANISMQUANTITYTYPE,
	u.ORIGINOFMATERIAL,
	u.CollectionTwoMATERIAL,
	u.GARDENSMATERIAL,
	u.HORTLABELSUFFIX,
	u.ETHNOFLAG,
	u.EXSIC,
	u.EXSICCATAFASCICLE,
	u.EXSICCATANUMBER,
	u.CollectionTwoMATERIALREQUIREDFLAG,
	u.CollectionTwoMATERIALREQDDESCRIPTION,
	u.INTERNALNOTE,
	u.OCCURRENCEREMARKS,
	u.CREATEDBY,
	u.CREATEDATE,
	u.UPDATEDBY,
	u.UPDATEDATE,
	t.PRIMARYNAME
	FROM CMS_COLLECTION_UNIT_V u LEFT JOIN CMS_COLLECTING_EVENT_V e USING (LEGACYID_CE)
	LEFT JOIN CMS_COLLECTING_TEAM_V t USING (LEGACYID_CTM)
;
