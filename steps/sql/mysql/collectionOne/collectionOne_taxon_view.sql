CREATE OR REPLACE VIEW cms_taxon_view AS
	SELECT
		t.LEGACYID_TAXON,
		t.LEGACYID_PT,
		t.LEGACYID_PT2,
		t.SCIENTIFICNAME,
		p.SCIENTIFICNAME as P_SCIENTIFICNAME,
		p2.SCIENTIFICNAME as P2_SCIENTIFICNAME,
		t.SIMPLENAME,
		t.SCIENTIFICNAMEAUTHORSHIP,
		t.SCIENTIFICNAMEID,
		t.NAMEPUBLISHEDINYEAR,
		t.LEGACYID_RANK,
		t.VERNACULARNAME,
		t.CULTIVARREGISTRATIONTYPE,
		t.CULTIVARGROUPNAME,
		t.CULTIVARNAME,
		t.TRADEDESIGNATION,
		t.HYBRIDFLAG,
		t.TAXONOMICSTATUS,
		t.NOMENCLATURALSTATUS,
		t.CHANGEDCOMBINATION,
		t.TAXONREMARKS,
		t.TAGNAME,
		t.ACCESSSTATUS,
		t.ACCESSREASON,
		t.CREATEDBY,
		t.CREATEDATE,
		t.UPDATEDBY,
		t.UPDATEDATE,
		tr.TAXONRANK,
		tr.NOMENCLATURALCODE,
		tr.TAXONRANKCODE,
		tr.TAXONRANKORDER,
		tr.CREATEDBY as TR_CREATEDBY,
		tr.CREATEDATE as TR_CREATEDATE,
		tr.UPDATEDBY as TR_UPDATEDBY,
		tr.UPDATEDATE as TR_UPDATEDATE
	FROM
		CMS_TAXON_V t LEFT JOIN CMS_TAXON_RANK_V tr on t.LEGACYID_RANK = tr.LEGACYID_RANK
		LEFT JOIN CMS_TAXON_V p on p.LEGACYID_TAXON = t.LEGACYID_PT
		LEFT JOIN CMS_TAXON_V p2 on p2.LEGACYID_TAXON = t.LEGACYID_PT2
